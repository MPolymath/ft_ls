#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c <= 066 && c >= 060) || (c <= 'z' && c >= 'A'))
	{
		if ((c <= 'z' && c >= 'a') || (c <= 'Z' && c >= 'A'))
			return (1);
		if (c <= 071 && c >= 060)
			return (1);
	}
	return (0);
}
